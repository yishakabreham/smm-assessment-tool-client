package com.yishak.smm_assessment.activities.buffer;

public class AssessmentProgress
{
    private int phase_id;
    private int practice_id;
    private int sub_practice_id;
    private int implementation_level;

    public int getPhase_id() {
        return phase_id;
    }

    public void setPhase_id(int phase_id) {
        this.phase_id = phase_id;
    }

    public int getPractice_id() {
        return practice_id;
    }

    public void setPractice_id(int practice_id) {
        this.practice_id = practice_id;
    }

    public int getSub_practice_id() {
        return sub_practice_id;
    }

    public void setSub_practice_id(int sub_practice_id) {
        this.sub_practice_id = sub_practice_id;
    }

    public int getImplementation_level() {
        return implementation_level;
    }

    public void setImplementation_level(int implementation_level) {
        this.implementation_level = implementation_level;
    }
}
